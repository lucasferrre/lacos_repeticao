//lgoritmo que mostra uma contagem crescente do 6 ao 12
let i = 6;

//exemplo while
console.log("Exemplo WHILE!!!");
while (i <= 12) {
    console.log(`número ${i}`);
    i = i + 1;
}
console.log("FIM!");

//exemplo for
console.log("Exemplo FOR!!!");
for (i = 6; i <= 13; i++) {
    console.log(`número ${i}`);
}
console.log("FIM!");

//exemplo Do...While
console.log("Exemplo DO...WHILE");
i = 6;
do {
    console.log(`número ${i}`);
    i = i + 1;
} while (i <= 12);
console.log("FIM!");
